select TOP 1
	TO_TIMESTAMP(TO_VARCHAR("TIME", 'YYYY-MM-DD HH24:MI')) "TIME",
	HOST,
	MAX(CPU) "CPU_PCT",
	ROUND(MAX(MEMORY_USED)/1024/1024/1024, 1) "HANA_USED_MEMORY_GB",
	ROUND(MAX(MEMORY_RESIDENT)/1024/1024/1024, 1) "HANA_RESIDENT_MEMORY_GB",
	ROUND(MAX(MEMORY_ALLOCATION_LIMIT)/1024/1024/1024, 1) "HANA_ALLOCATION_LIMIT_GB",
	ROUND(MAX(MEMORY_TOTAL_RESIDENT)/1024/1024/1024, 1) "TOTAL_RESIDENT_MEMORY_GB",
	ROUND(MAX(MEMORY_SIZE)/1024/1024/1024, 1) "TOTAL_MEMORY_GB",
	ROUND(MAX(DISK_USED)/1024/1024/1024, 1) "DISK_USED_GB",
	ROUND(MAX(DISK_SIZE)/1024/1024/1024, 1) "DISK_SIZE_GB",
	MAX(NETWORK_IN) "NETWORK_IN_BYTE",
	MAX(NETWORK_OUT) "NETWORK_OUT_BYTE"
from 
	"M_LOAD_HISTORY_HOST"
group by
	TO_TIMESTAMP(TO_VARCHAR("TIME", 'YYYY-MM-DD HH24:MI')),
	HOST
order by
    TIME desc